def run_dressme(person_path, dress_path, user_instruction):
    if person_path is None or dress_path is None:
        return None, None, "Please upload both a person photo and a dress image.", pd.DataFrame()

    person_image = Image.open(person_path).convert("RGB")
    dress_image = Image.open(dress_path).convert("RGB")

    if not user_instruction or not user_instruction.strip():
        user_instruction = "Create a realistic virtual try-on preview and styling advice."

    analysis = analyze_person_and_dress(person_image, dress_image, user_instruction)
    tryon_path = generate_tryon_preview(person_image, dress_image, analysis, user_instruction)
    final_board = create_final_board(person_image, dress_image, tryon_path, analysis)

    markdown = format_analysis_markdown(analysis)

    return tryon_path, final_board, markdown, pd.DataFrame([analysis])


with gr.Blocks(title="DressMe AI — Virtual Dress Try-On Lite") as demo:
    gr.Markdown(
        "# DressMe AI — Virtual Dress Try-On Lite\n\n"
        "Upload your photo and a dress/product image. The app creates a visual try-on style preview and styling notes.\n\n"
        "Note: This is a concept preview, not an exact sizing or fit engine."
    )

    with gr.Row():
        with gr.Column(scale=1):
            person_input = gr.Image(type="filepath", label="Upload Your Photo", height=360)
            dress_input = gr.Image(type="filepath", label="Upload Dress/Product Image", height=360)
            instruction = gr.Textbox(
                label="Styling instruction",
                value="Create a virtual try-on preview for a wedding guest look. Keep it realistic and tasteful.",
                lines=3
            )
            run_button = gr.Button("Generate Try-On Preview", variant="primary")

        with gr.Column(scale=1):
            tryon_output = gr.Image(type="filepath", label="AI Try-On Preview")
            board_output = gr.Image(type="filepath", label="Shareable Result Board")

    analysis_output = gr.Markdown(label="Styling Analysis")
    raw_output = gr.Dataframe(label="Raw Structured Output", wrap=True)

    run_button.click(
        fn=run_dressme,
        inputs=[person_input, dress_input, instruction],
        outputs=[tryon_output, board_output, analysis_output, raw_output]
    )


demo.queue(default_concurrency_limit=1).launch(
    debug=False,
    share=True,
    prevent_thread_lock=True,
    show_error=True
)

