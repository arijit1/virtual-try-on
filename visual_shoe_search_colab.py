import gradio as gr

def run_visual_shopping_search(image, user_instruction, region, num_results):
    if image is None:
        return None, "Please upload a shoe image.", pd.DataFrame(), ""

    if not user_instruction or not user_instruction.strip():
        user_instruction = "Find similar shoes online"

    attributes = extract_shoe_attributes(image, user_instruction)

    results = search_similar_shoes(
        attributes=attributes,
        user_instruction=user_instruction,
        max_results=int(num_results),
        region=region
    )

    ranking = rank_results_with_llm(attributes, results, user_instruction)

    rows = []
    for i, item in enumerate(results, start=1):
        rows.append({
            "Rank": i,
            "Title": item.get("title"),
            "Source": item.get("source"),
            "Snippet": item.get("snippet"),
            "Type": item.get("result_type"),
            "Link": item.get("link"),
            "Thumbnail": item.get("thumbnail"),
        })

    df_results = pd.DataFrame(rows)

    query = attributes.get("search_query", "")

    markdown = (
        "## Detected Shoe Attributes\n\n"
        "```json\n"
        + json.dumps(attributes, indent=2)
        + "\n```\n\n"
        + "## Generated Search Query\n\n"
        + f"`{query}`\n\n"
        + "## LLM Ranking Summary\n\n"
        + ranking
    )

    return image, markdown, df_results, make_cards_html(results)


