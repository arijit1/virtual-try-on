"""## 2. Image helper"""

def pil_image_to_data_url(image: Image.Image, max_size: int = 768) -> str:
    image = image.convert("RGB")
    image.thumbnail((max_size, max_size))

    buffer = io.BytesIO()
    image.save(buffer, format="JPEG", quality=85)
    encoded = base64.b64encode(buffer.getvalue()).decode("utf-8")

    return f"data:image/jpeg;base64,{encoded}"

"""## 3. Extract shoe attributes with retry + fallback"""

def extract_shoe_attributes(
    image: Image.Image,
    user_instruction: str = "",
    max_retries: int = 3
) -> dict:
    data_url = pil_image_to_data_url(image, max_size=768)

    system_prompt = """
You are a fashion-tech visual shopping assistant.
Analyze a shoe image and create useful shopping-search attributes.
Do not identify a brand unless the logo or text is clearly visible.
Return only valid JSON. No markdown.
"""

    user_prompt = f"""
Analyze this uploaded shoe image.

User shopping instruction:
{user_instruction}

Return JSON with these keys:
- product_type
- primary_colors
- secondary_colors
- silhouette
- material_guess
- sole_type
- closure_type
- style_keywords
- occasion
- gender_guess
- visible_brand_or_logo
- confidence_notes
- search_query
- image_search_query
- negative_keywords

Rules:
- search_query should be useful for web shopping search.
- image_search_query should be useful for image search.
- Include user's constraints such as budget, country, gender, or brand preference if provided.
- For India queries, include India, online, buy, and INR-style terms when useful.
- Do not overclaim material or brand.
- Keep the search query short and specific.
"""

    last_error = None

    for attempt in range(max_retries):
        try:
            response = client.chat.completions.create(
                model="gpt-4.1-mini",
                messages=[
                    {"role": "system", "content": system_prompt},
                    {
                        "role": "user",
                        "content": [
                            {"type": "text", "text": user_prompt},
                            {
                                "type": "image_url",
                                "image_url": {
                                    "url": data_url,
                                    "detail": "low"
                                }
                            }
                        ]
                    }
                ],
                temperature=0.2,
                response_format={"type": "json_object"}
            )

            raw = response.choices[0].message.content.strip()
            raw = re.sub(r"^```json\s*", "", raw)
            raw = re.sub(r"^```\s*", "", raw)
            raw = re.sub(r"\s*```$", "", raw)
            return json.loads(raw)

        except (InternalServerError, APIConnectionError, RateLimitError, APIStatusError) as e:
            last_error = e
            print(f"OpenAI API error on attempt {attempt + 1}/{max_retries}: {e}")
            time.sleep(2 ** attempt)

        except json.JSONDecodeError as e:
            last_error = e
            print(f"JSON parse error on attempt {attempt + 1}/{max_retries}: {e}")
            time.sleep(2 ** attempt)

    print("Vision analysis failed after retries. Using fallback query.")

    fallback_instruction = user_instruction.strip() or "similar shoes online"

    return {
        "product_type": "shoes",
        "primary_colors": [],
        "secondary_colors": [],
        "silhouette": "unknown",
        "material_guess": [],
        "sole_type": "unknown",
        "closure_type": "unknown",
        "style_keywords": ["similar shoes"],
        "occasion": "unknown",
        "gender_guess": "unknown",
        "visible_brand_or_logo": "unknown",
        "confidence_notes": f"OpenAI vision call failed after retries: {last_error}",
        "search_query": f"buy similar shoes online {fallback_instruction}",
        "image_search_query": f"similar shoes {fallback_instruction}",
        "negative_keywords": []
    }

"""\## 4. Search the internet"""

def ddg_text_search(query: str, max_results: int = 10, region: str = "in-en") -> list:
    results = []
    try:
        with DDGS() as ddgs:
            for item in ddgs.text(
                query,
                region=region,
                safesearch="moderate",
                max_results=max_results
            ):
                href = item.get("href")
                source = None
                if href:
                    try:
                        source = href.split("/")[2]
                    except Exception:
                        source = href

                results.append({
                    "result_type": "web",
                    "title": item.get("title"),
                    "snippet": item.get("body"),
                    "link": href,
                    "source": source,
                    "thumbnail": None,
                })
    except Exception as e:
        print(f"DDG text search failed: {e}")

    return results


def ddg_image_search(query: str, max_results: int = 12, region: str = "in-en") -> list:
    results = []
    try:
        with DDGS() as ddgs:
            for item in ddgs.images(
                query,
                region=region,
                safesearch="moderate",
                max_results=max_results
            ):
                results.append({
                    "result_type": "image",
                    "title": item.get("title"),
                    "snippet": item.get("source"),
                    "link": item.get("url"),
                    "source": item.get("source"),
                    "thumbnail": item.get("thumbnail") or item.get("image"),
                })
    except Exception as e:
        print(f"DDG image search failed: {e}")

    return results


def search_similar_shoes(
    attributes: dict,
    user_instruction: str,
    max_results: int = 12,
    region: str = "in-en"
) -> list:
    web_query = attributes.get("search_query") or "similar shoes online"
    image_query = attributes.get("image_search_query") or web_query

    if "buy" not in web_query.lower() and "online" not in web_query.lower():
        web_query = f"buy {web_query} online"

    image_results = ddg_image_search(image_query, max_results=max_results, region=region)
    web_results = ddg_text_search(web_query, max_results=max_results, region=region)

    merged = image_results + web_results

    seen = set()
    deduped = []
    for item in merged:
        key = item.get("link") or item.get("title")
        if key and key not in seen:
            seen.add(key)
            deduped.append(item)

    return deduped[:max_results]

"""## 5. Rank results with ChatGPT"""

def rank_results_with_llm(attributes: dict, results: list, user_instruction: str = "") -> str:
    if not results:
        return "No search results found to rank."

    compact_results = []
    for i, item in enumerate(results, start=1):
        compact_results.append({
            "rank": i,
            "title": item.get("title"),
            "source": item.get("source"),
            "snippet": item.get("snippet"),
            "link": item.get("link"),
            "result_type": item.get("result_type"),
        })

    prompt = f"""
You are a fashion-tech shopping assistant.

Uploaded shoe attributes:
{json.dumps(attributes, indent=2)}

User request:
{user_instruction}

Search results:
{json.dumps(compact_results, indent=2)}

Tasks:
1. Pick the top 3 most visually or semantically similar results.
2. Explain briefly why each is a good match.
3. Mention if a result is approximate.
4. Remind the user to manually verify product details before purchase.

Keep the response concise.
"""

    try:
        response = client.chat.completions.create(
            model="gpt-4.1-mini",
            messages=[
                {"role": "system", "content": "You are a concise fashion shopping result ranking assistant."},
                {"role": "user", "content": prompt}
            ],
            temperature=0.2
        )
        return response.choices[0].message.content

    except Exception as e:
        return f"Ranking failed, but search results are still shown. Error: {e}"

"""## 6. Visual product cards"""

def safe_text(value):
    return html.escape(str(value)) if value is not None else ""


def make_cards_html(results):
    if not results:
        return "<h3>No results found.</h3>"

    card_html = """
    <div style="
        display: grid;
        grid-template-columns: repeat(auto-fill, minmax(230px, 1fr));
        gap: 16px;
        font-family: Arial, sans-serif;
    ">
    """

    for item in results:
        img = safe_text(item.get("thumbnail") or "")
        title_text = safe_text(item.get("title") or "Untitled result")
        source = safe_text(item.get("source") or "Unknown source")
        snippet = safe_text((item.get("snippet") or "")[:150])
        link = safe_text(item.get("link") or "#")
        result_type = safe_text(item.get("result_type") or "result")

        if img:
            image_html = f'<img src="{img}" style="max-height: 170px; max-width: 100%; object-fit: contain;" />'
        else:
            image_html = '<span style="color:#999;">No image</span>'

        card_html += f"""
        <div style="
            border: 1px solid #e5e5e5;
            border-radius: 14px;
            padding: 12px;
            box-shadow: 0 2px 8px rgba(0,0,0,0.08);
            background: white;
        ">
            <div style="height: 180px; display:flex; align-items:center; justify-content:center; background:#fafafa; border-radius:10px;">
                {image_html}
            </div>
            <p style="font-size: 12px; color: #777; text-transform: uppercase;">{result_type}</p>
            <h3 style="font-size: 15px; line-height: 1.3; min-height: 56px;">{title_text}</h3>
            <p style="color: #555; margin: 6px 0;">{source}</p>
            <p style="color: #777; font-size: 13px; line-height: 1.3; min-height: 48px;">{snippet}</p>
            <a href="{link}" target="_blank" style="
                display: inline-block;
                margin-top: 8px;
                padding: 8px 10px;
                border-radius: 8px;
                text-decoration: none;
                background: #111;
                color: white;
            ">Open result</a>
        </div>
        """

    card_html += "</div>"
    return card_html

